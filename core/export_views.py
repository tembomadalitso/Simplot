@require_GET
@login_required
def zra_export_excel(request):
    data     = _get_zra_data(request)
    date_str = NOW().strftime('%d %B %Y')
    rpt_id   = NOW().strftime('TTR-%Y%m%d%H%M')

    wb = openpyxl.Workbook()
    ws = wb.active
    ws.title = "Turnover Tax Report"

    _xl_banner(ws, "Turnover Tax Report",
               f"ZRA Official Portal     Report ID: {rpt_id}", 8, date_str)

    # ── KPI block (rows 4-6) ──
    tots = [len(data),
            sum(r['total'] for r in data),
            sum(r['tax']   for r in data),
            sum(r['after_tax'] for r in data)]
    kpi_labels = ["Total Landlords","Total Revenue (ZMW)","Tax Collected (ZMW)","Net After Tax (ZMW)"]
    kpi_cols   = [HEX['primary'], HEX['success'], HEX['warning'], HEX['sl900']]
    for i, (lbl, val, col) in enumerate(zip(kpi_labels, tots, kpi_cols)):
        sc = i * 2 + 1
        for row_n in (4, 5, 6):
            ws.merge_cells(start_row=row_n, start_column=sc, end_row=row_n, end_column=sc+1)
        c4 = ws.cell(row=4, column=sc, value=lbl)
        c4.font=Font(size=8,color=HEX['sl700'],name='Arial'); c4.fill=_solid(HEX['sl100'])
        c4.alignment=Alignment(horizontal='center',vertical='center')
        ws.row_dimensions[4].height=14

        c5 = ws.cell(row=5, column=sc, value=val)
        c5.font=Font(bold=True,size=14,color=col,name='Arial'); c5.fill=_solid(HEX['sl50'])
        c5.alignment=Alignment(horizontal='center',vertical='center')
        if i > 0: c5.number_format='"ZMW "#,##0.00'
        ws.row_dimensions[5].height=24
        ws.row_dimensions[6].height=8

    # ── Header row (row 7) ──
    COLS = ["Landlord Name","ID","Count of Apartments","Rent Amount",
            "Total Amount","Tax Rate","Tax Amount","Amount after Tax"]
    HDR_ROW = 7
    _xl_header_row(ws, COLS, HDR_ROW)

    # ── Data rows ──
    DS = HDR_ROW + 1
    for ri, r in enumerate(data):
        rn   = DS + ri
        fill = HEX['white'] if ri % 2 == 0 else HEX['sl50']
        vals = [r['landlord'], r['id'], r['apartments'],
                r['rent'], r['total'], r['rate'], r['tax'], r['after_tax']]
        for ci, val in enumerate(vals, 1):
            c = ws.cell(row=rn, column=ci, value=val)
            c.font   = Font(size=9, name='Arial', color=HEX['sl700'])
            c.fill   = _solid(fill)
            c.border = _brd(HEX['sl200'])
            c.alignment = Alignment(
                horizontal='center' if ci==2 else 'right' if ci in(3,4,5,7,8) else 'left',
                vertical='center')
            if ci in (4,5,7,8): c.number_format='"ZMW "#,##0.00'
            elif ci==6:          c.number_format='0.0%'
            if ci==7: c.font=Font(size=9,name='Arial',color=HEX['warning'],bold=True)
            if ci==8: c.font=Font(size=9,name='Arial',color=HEX['success'],bold=True)
        ws.row_dimensions[rn].height = 18

    # ── Totals row ──
    TROW = DS + len(data)
    for ci, (val, align, col, fmt) in enumerate([
        ("TOTALS","left", HEX['white'],False),
        ("","center",     HEX['white'],False),
        (f'=SUM(C{DS}:C{TROW-1})',"right",HEX['white'],False),
        ("","right",      HEX['white'],False),
        (f'=SUM(E{DS}:E{TROW-1})',"right",HEX['white'],True),
        ("","center",     HEX['white'],False),
        (f'=SUM(G{DS}:G{TROW-1})',"right",HEX['warning'],True),
        (f'=SUM(H{DS}:H{TROW-1})',"right",HEX['success'],True),
    ], 1):
        c=ws.cell(row=TROW,column=ci,value=val)
        c.font=Font(bold=True,size=9,name='Arial',color=col)
        c.fill=_solid(HEX['dark_green'])
        c.alignment=Alignment(horizontal=align,vertical='center')
        c.border=_mid_top(HEX['success'])
        if fmt: c.number_format='"ZMW "#,##0.00'
    ws.row_dimensions[TROW].height=22

    for i,w in enumerate([30,10,18,20,20,10,20,22],1):
        ws.column_dimensions[get_column_letter(i)].width=w
    ws.freeze_panes=f"A{DS}"
    ws.auto_filter.ref=f"A{HDR_ROW}:H{TROW-1}"
    ws.page_setup.orientation=ws.ORIENTATION_LANDSCAPE
    ws.page_setup.paperSize=ws.PAPERSIZE_A4
    ws.page_setup.fitToWidth=1
    ws.print_title_rows=f"$1:${HDR_ROW}"
    ws.sheet_view.showGridLines=False

    buf = io.BytesIO()
    wb.save(buf)
    buf.seek(0)
    fname = f"ZRA_Turnover_Tax_{NOW().strftime('%Y-%m-%d')}.xlsx"
    resp  = HttpResponse(buf,
        content_type='application/vnd.openxmlformats-officedocument.spreadsheetml.sheet')
    resp['Content-Disposition'] = f'attachment; filename="{fname}"'
    return resp


