# ── PDF chrome drawn on every page ──────────────────────────────
def _draw_chrome(canv, doc, portal_label):
    w, h = doc.pagesize
    canv.setFillColor(C['primary'])
    canv.rect(0, h - 16*mm, w, 16*mm, fill=1, stroke=0)
    canv.setFillColor(C['success'])
    canv.rect(0, h - 17*mm, w, 1*mm, fill=1, stroke=0)
    canv.setFont('Helvetica-Bold', 11)
    canv.setFillColor(C['white'])
    canv.drawString(15*mm, h - 10*mm, 'ZAMBIA RENTALS SYSTEM')
    canv.setFont('Helvetica', 8)
    canv.setFillColor(colors.HexColor('#c7d2fe'))
    canv.drawRightString(w - 15*mm, h - 10*mm, portal_label)


# ── Numbered canvas for page X of Y ─────────────────────────────
class _NumCanvas(rl_canvas.Canvas):
    def __init__(self, *args, **kwargs):
        super().__init__(*args, **kwargs)
        self._saved = []

    def showPage(self):
        self._saved.append(dict(self.__dict__))
        self._startPage()

    def save(self):
        n = len(self._saved)
        for state in self._saved:
            self.__dict__.update(state)
            self._footer(n)
            super().showPage()
        super().save()

    def _footer(self, n):
        w, h = self._pagesize
        ts = NOW().strftime('%d %b %Y  %H:%M')
        self.setFillColor(C['slate_100'])
        self.rect(0, 0, w, 9*mm, fill=1, stroke=0)
        self.setFont('Helvetica', 7)
        self.setFillColor(C['slate_500'])
        self.drawString(15*mm, 3*mm,
            f"Generated by Zambia Rentals System  |  {ts}  |  Confidential")
        self.drawRightString(w - 15*mm, 3*mm, f"Page {self._pageNumber} of {n}")


def _make_doc(buf, pagesize, portal_label):
    doc = BaseDocTemplate(
        buf, pagesize=pagesize,
        leftMargin=15*mm, rightMargin=15*mm,
        topMargin=20*mm, bottomMargin=13*mm,
    )
    frame = Frame(doc.leftMargin, doc.bottomMargin, doc.width, doc.height, id='body')
    doc.addPageTemplates([PageTemplate(
        id='main', frames=[frame],
        onPage=lambda c, d: _draw_chrome(c, d, portal_label),
    )])
    return doc


def _ps(name, **kw): return ParagraphStyle(name, **kw)


# ── Excel banner row ─────────────────────────────────────────────
def _xl_banner(ws, title, sub, ncols, date_str):
    ws.merge_cells(start_row=1, start_column=1, end_row=1, end_column=ncols)
    c = ws['A1']
    c.value     = f"ZAMBIA RENTALS SYSTEM — {title.upper()}"
    c.font      = Font(bold=True, size=14, color=HEX['white'], name='Arial')
    c.fill      = _solid(HEX['primary'])
    c.alignment = Alignment(horizontal='center', vertical='center')
    ws.row_dimensions[1].height = 30

    ws.merge_cells(start_row=2, start_column=1, end_row=2, end_column=ncols)
    c = ws['A2']
    c.value     = sub + f"     Generated: {date_str}"
    c.font      = Font(italic=True, size=9, color=HEX['sl700'], name='Arial')
    c.fill      = _solid(HEX['primary_lt'])
    c.alignment = Alignment(horizontal='center', vertical='center')
    ws.row_dimensions[2].height = 15
    ws.row_dimensions[3].height = 8


def _xl_header_row(ws, cols, row_n):
    for ci, h in enumerate(cols, 1):
        c = ws.cell(row=row_n, column=ci, value=h)
        c.font      = Font(bold=True, size=9, color=HEX['white'], name='Arial')
        c.fill      = _solid(HEX['primary'])
        c.alignment = Alignment(horizontal='center', vertical='center', wrap_text=True)
        c.border    = Border(bottom=Side(style='medium', color=HEX['success']))
    ws.row_dimensions[row_n].height = 28


